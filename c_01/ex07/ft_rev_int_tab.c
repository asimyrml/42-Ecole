void	ft_rev_int_tab(int *tab, int size)
{
	int	i;
	int	j;
	int	val;

	i = 0;
	j = size - 1;
	while (i < j)
	{
		val = tab[i];
		tab[i] = tab[j];
		tab[j] = val;
		i++;
		j--;
	}
}
